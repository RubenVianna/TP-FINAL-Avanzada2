    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        //paso todos los controles
        
        int dni = Integer.parseInt(txtDni.getText());
        
       
        Cliente clienteTemporal = this.controlador.buscarClienteBDD(dni);
        if(clienteTemporal == null){
            TipoCliente tc = this.controlador.buscarTipoCliente("Inscripto");
            Cliente cli = new Cliente (dni, txtNombre.getText(), txtApellido.getText(),txtTelefono.getText(), txtDireccion.getText(), tc);
            
            this.controlador.persistirCliente(cli);
            
            //vaciamos los campos de la interfaz y desactivamos el boton aceptar
                    txtDni.setText("");
                    txtNombre.setText("");
                    txtApellido.setText("");
                    txtTelefono.setText("");
                    txtDireccion.setText("");
                    botonAceptar.setEnabled(false);
            JOptionPane.showMessageDialog(this, "El cliente fue cargado existosamente ", "Aviso", INFORMATION_MESSAGE);
         }else{
            JOptionPane.showMessageDialog(this, "El dni ingresado corresponde con un cliente ya registrado ", "Error", ERROR_MESSAGE);
            //vaciamos los campos de la interfaz y desactivamos el boton aceptar
                    txtDni.setText("");
                    txtNombre.setText("");
                    txtApellido.setText("");
                    txtTelefono.setText("");
                    txtDireccion.setText("");
                    botonAceptar.setEnabled(false);
        }      
         
          
    }//GEN-LAST:event_botonAceptarActionPerformed

